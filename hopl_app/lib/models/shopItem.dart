class ShopItem {
  final String name;
  final double price;
  final int stockQuantity;
  final String imgUrl;
  final String category;
  final String productId;
  final String shopId;

  ShopItem({
    required this.name,
    required this.price,
    required this.stockQuantity,
    required this.imgUrl,
    required this.category,
    required this.productId,
    required this.shopId,
  });
}
